local ls = require("luasnip")
local s = ls.snippet
local t = ls.text_node
local i = ls.insert_node

return {
  ------------------------------------------------------------------
  -- 标题
  ------------------------------------------------------------------
  s("h1", { t("# "), i(1) }),
  s("h2", { t("## "), i(1) }),
  s("h3", { t("### "), i(1) }),
  s("h4", { t("#### "), i(1) }),
  s("h5", { t("##### "), i(1) }),
  s("h6", { t("###### "), i(1) }),

  ------------------------------------------------------------------
  -- 代码块
  ------------------------------------------------------------------
  s("cb", {
    t("```"),
    i(1, "lang"),
    t({ "", "" }),
    i(2),
    t({ "", "```" }),
  }),

  ------------------------------------------------------------------
  -- 图片 & 链接
  ------------------------------------------------------------------
  s("img", {
    t("!["),
    i(1, "alt"),
    t("]("),
    i(2, "./"),
    t(")"),
  }),

  s("link", {
    t("["),
    i(1, "text"),
    t("]("),
    i(2, "url"),
    t(")"),
  }),

  ------------------------------------------------------------------
  -- TODO 列表
  ------------------------------------------------------------------
  s("todo", { t("- [ ] "), i(1) }),
  s("done", { t("- [x] "), i(1) }),

  ------------------------------------------------------------------
  -- 表格（3 列，最常用）
  ------------------------------------------------------------------
  s("tbl", {
    t("| "),
    i(1, "Header 1"),
    t(" | "),
    i(2, "Header 2"),
    t(" | "),
    i(3, "Header 3"),
    t({ " |", "| --- | --- | --- |", "| " }),
    i(4, "Cell 1"),
    t(" | "),
    i(5, "Cell 2"),
    t(" | "),
    i(6, "Cell 3"),
    t(" |"),
  }),

  ------------------------------------------------------------------
  -- 表格（可继续 Tab 扩展填写，写论文/README 很爽）
  ------------------------------------------------------------------
  s("table", {
    t("| "),
    i(1, "H1"),
    t(" | "),
    i(2, "H2"),
    t(" | "),
    i(3, "H3"),
    t({ " |", "| --- | --- | --- |", "| " }),
    i(4),
    t(" | "),
    i(5),
    t(" | "),
    i(6),
    t(" |"),
  }),
}

